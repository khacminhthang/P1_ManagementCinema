    public void process() {
        String name = jTextFieldUserName.getText();
        List<User> users = null;

        try {
            if (name.trim().length() == 0) {
                users = loginDAO.findAll();
            } else {
                users = loginDAO.findAllMovieByName(name);
            }
           
            UserTableModel model = new UserTableModel(users);
            jTable1.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Loi " + e);
        }

    }

