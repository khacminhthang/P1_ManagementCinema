    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        try {
            int row = jTable1.getSelectedRow();

            if (row < 0) {
                JOptionPane.showMessageDialog(this,
                        "You must select an movie", "Error", JOptionPane.ERROR_MESSAGE);
                return;

            }
            int response
                    = JOptionPane.showConfirmDialog(this, "Delete this User?", "Confirm",
                            JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (response != JOptionPane.YES_OPTION) {
                return;
            }
            User tempUser = (User) jTable1.getValueAt(row, UserTableModel.OBJECT_COL);

            loginDAO.deleteUser(tempUser.getId());
            process();
            JOptionPane.showMessageDialog(this,
                    "User deleted succesfully.", "Movie Deleted",
                    JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                    "Error deleting user: " + e.getMessage(), "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

