    private void jTextFieldSearchKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldSearchKeyPressed
        // TODO add your handling code here:
        switch(evt.getKeyCode()){
            case KeyEvent.VK_BACK_SPACE:
                break;
            case KeyEvent.VK_ENTER:
                jTextFieldSearch.setText(jTextFieldSearch.getText());
                break;
            default:
                EventQueue.invokeLater(new Runnable() {
            @Override
            public void run() {
                try {
                    String txt = jTextFieldSearch.getText();
                    autoComplete(txt);
                } catch (SQLException ex) {
                    Logger.getLogger(MovieGUI.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        });
                
        }
    }//GEN-LAST:event_jTextFieldSearchKeyPressed
    public void autoComplete(String txt) throws SQLException{
        String complete= "";
        int start = txt.length();
        int last = txt.length();
        int a;
        name = movieDAO.databaseName();
        for(a=0;a<name.size();a++){
            if(name.get(a).toString().startsWith(txt)){
                complete = name.get(a).toString();
                last = complete.length();
                break;
                
            }
        }
        if(last>start){
            jTextFieldSearch.setText(complete);
            jTextFieldSearch.setCaretPosition(last);
            jTextFieldSearch.moveCaretPosition(start);
        }
        
    }
