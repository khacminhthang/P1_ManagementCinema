    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        try {
            int row = jTable1.getSelectedRow();

            if (row < 0) {
                JOptionPane.showMessageDialog(this,
                        "You must select an movie", "Error", JOptionPane.ERROR_MESSAGE);
                return;

            }
            int response
                    = JOptionPane.showConfirmDialog(this, "Delete this Movie?", "Confirm",
                            JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (response != JOptionPane.YES_OPTION) {
                return;
            }
            Movie tempMovie = (Movie) jTable1.getValueAt(row, MovieTableModel.OBJECT_COL);

            movieDAO.deleteMovie(tempMovie.getId());
            process();
            JOptionPane.showMessageDialog(this,
                    "Movie deleted succesfully.", "Movie Deleted",
                    JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                    "Error deleting movie: " + e.getMessage(), "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

